package javasrc;

public class User {
    String firstName;
    String lastName;
    String email;
    String address;
    String phone;
    String password;

    public User(String firstName, String lastName, String email, String address, String phone, String password) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.address = address;
        this.phone = phone;
        this.password = password;
    }
}
